package Uebungen_in_LV;

public class Uebung1_Ausgabe {

	public static void main(String[] args) {
		int a =9;
		int b=5;
		
		System.out.println(a+" + "+b+" = " + (a+b));
		System.out.println(a+" - "+b+" = " + (a-b));
		System.out.println(a+" * "+b+" = " + (a*b));
		System.out.println(a+" / "+b+" = " + (a/b));
		System.out.println(a+" % "+b+" = " + (a%b));
		
	}

}
